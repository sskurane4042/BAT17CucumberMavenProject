package javapackage;

public class SwitchClass {
	
	public static void main(String[] args) {
		
		int number=20;
	switch (number)    
	{
	case 10:
	
	    System.out.println("Print 10 value is correct");	
	break;                                                   //break is used to stop continuous of output. it stoping
	case 15:
	
		System.out.println("Print 15 value is correct");
		break;
	case 20:
		
	
		System.out.println("Print 20 value is correct");
	break;
	
	default:
	    System.out.println("Print value not matching");
	}
	}
	}